#include "../includes/fillit.h"

void	adv_xy(int board_len, int *x, int *y)
{
	*y += (++*x / board_len);
	*x %= board_len;
}

void	zero_out(int *a, int *b, int *c)
{
	*a = 0;
	*b = 0;
	*c = 0;
}
