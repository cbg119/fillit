#include "../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		length;
	char	*result;

	length = ft_strlen(s1);
	if (!(result = (char *)malloc(sizeof(char) * length + 1)))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
